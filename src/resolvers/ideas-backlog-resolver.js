/**
 * Ideas Backlog Resolver — export-ideas-backlog action
 *
 * Builds the two-tier Ideas Backlog hierarchy in Confluence:
 *
 *   💡 Gustiel Ideas Backlog          ← Root page (organizer, no body content)
 *     ├── <Idea title A>               ← per-idea pages (full content)
 *     ├── <Idea title B>
 *     └── <Idea title C>               ← sorted alphabetically by title
 *
 * UNLIKE Release Notes, this export uses **destructive sync** scoped strictly
 * to the immediate children of the backlog root: any child whose title does
 * not match a current idea file in `docs/ideas/` is DELETED. This keeps the
 * Confluence backlog mirror-synced with the source folder — when an idea ships
 * (or is dropped) and you delete its `.md` file, the next export removes the
 * Confluence page too.
 *
 * Trade-off: deleting a Confluence page also deletes its comments. This is
 * an accepted consequence — feedback on ideas is collected via chat / talk,
 * not Confluence comments.
 *
 * Source data: IDEAS export from src/docs/index.js, generated by
 * scripts/bundle-docs.mjs from docs/ideas/MAJOR.MINOR.md files.
 */

import { getEnvFromJira }                                from '../services/jira-api-service.js';
import { getSpaceByKey, findPageByTitle, createConfluencePage,
         updateConfluencePage, findOrCreatePageByPath,
         getPageChildren, deleteConfluencePage }         from '../services/confluence-api-service.js';
import { markdownToStorage, buildTocMacro,
         buildAnchorMacro }                              from '../formatters/confluence-formatter.js';
import { IDEAS }                                         from '../docs/index.js';

// ── Stable titles ────────────────────────────────────────────────────────────

const ROOT_TITLE = '💡 Gustiel Ideas Backlog';

// ── Page body builders ───────────────────────────────────────────────────────

function buildRootMarkdown() {
    return [
        `# 💡 Gustiel — Ideas Backlog`,
        ``,
        `Possible features and improvements being explored. **Nothing here is committed** —`,
        `these are exploratory ideas open for discussion. Feedback welcome via chat or talk.`,
        ``,
        `Browse the child pages below for individual ideas. When an idea ships, it moves`,
        `to the **📣 Gustiel Release Notes**; when it's dropped, the page disappears entirely.`,
    ].join('\n');
}

function buildIdeaMarkdown(idea) {
    const meta = [];
    if (idea.status)  meta.push(`**Status:** ${idea.status}`);
    if (idea.created) meta.push(`**Created:** ${idea.created}`);

    const lines = [`# ${idea.title}`, ``];
    if (meta.length) lines.push(`> ${meta.join(' · ')}`, ``);
    if (idea.body)   lines.push(idea.body);
    return lines.join('\n');
}

// ── Upsert helper (same 3-tier pattern used by other doc resolvers) ──────────

async function upsertPage(spaceId, title, markdown, parentId, parentIsFolder, withToc) {
    const anchor      = buildAnchorMacro('top');
    const toc         = withToc ? buildTocMacro(1, 3) + '\n<hr/>\n' : '';
    const fullContent = markdownToStorage(markdown, { uniformColumns: true });
    const storageBody = `<p>${anchor}</p>\n${toc}${fullContent}`;

    const existingAtTarget = await findPageByTitle(spaceId, title, parentId, parentIsFolder);
    if (existingAtTarget) {
        return await updateConfluencePage(existingAtTarget.id, existingAtTarget.version.number, spaceId, title, storageBody, parentId || null);
    }
    const existingElsewhere = await findPageByTitle(spaceId, title);
    if (existingElsewhere) {
        return await updateConfluencePage(existingElsewhere.id, existingElsewhere.version.number, spaceId, title, storageBody, parentId || null);
    }
    const stub = await createConfluencePage(spaceId, title, '<p>Generating…</p>', parentId);
    return await updateConfluencePage(stub.id, 1, spaceId, title, storageBody, parentId || null);
}

// ── Resolver ─────────────────────────────────────────────────────────────────

export const exportIdeasBacklog = async (event) => {
    const spaceKey   = (event?.payload?.spaceKey   || event?.spaceKey   || '').trim().toUpperCase();
    const parentPath = (event?.payload?.parentPath || event?.parentPath || '').trim();
    const folderId   = (event?.payload?.folderId   || event?.folderId   || '').trim();

    if (!spaceKey) return { status: 'ERROR', message: 'Please provide a Confluence space key (e.g. GUSTIEL).' };

    let env;
    try {
        env = await getEnvFromJira();
    } catch (e) {
        return { status: 'ERROR', message: `Could not detect Jira environment: ${e.message}` };
    }

    try {
        const space = await getSpaceByKey(spaceKey);

        // ── Resolve top-level parent (where the Ideas Backlog root lives) ─────
        let topParentId = null, topParentIsFolder = false;
        if (folderId) {
            topParentId       = folderId;
            topParentIsFolder = true;
        } else if (parentPath) {
            topParentId       = await findOrCreatePageByPath(space.id, parentPath);
            topParentIsFolder = false;
        }

        // ── Tier 1: Root (organizer, no body) ─────────────────────────────────
        const rootPage = await upsertPage(space.id, ROOT_TITLE, buildRootMarkdown(), topParentId, topParentIsFolder, false);

        // ── Sort current ideas alphabetically by title (case-insensitive) ─────
        const ideas = [...(IDEAS || [])].sort((a, b) => a.title.localeCompare(b.title, undefined, { sensitivity: 'base' }));
        const wantedTitles = new Set(ideas.map(i => i.title));

        // ── Destructive sync: delete orphan children (scoped to root) ─────────
        const existingChildren = await getPageChildren(rootPage.id);
        const deletedTitles    = [];
        for (const child of existingChildren) {
            if (!wantedTitles.has(child.title)) {
                try {
                    await deleteConfluencePage(child.id);
                    deletedTitles.push(child.title);
                } catch (e) {
                    deletedTitles.push(`${child.title} (⚠️ delete failed: ${e.message})`);
                }
            }
        }

        // ── Tier 2: Per-idea pages (upsert in alphabetical order) ─────────────
        const ideaResults = [];
        for (const idea of ideas) {
            const ideaPage = await upsertPage(space.id, idea.title, buildIdeaMarkdown(idea), rootPage.id, false, true);
            ideaResults.push({ title: idea.title, status: idea.status, pageId: ideaPage.id });
        }

        const rootUrl = `${env.baseUrl}/wiki${rootPage._links?.webui || `/spaces/${spaceKey}/pages/${rootPage.id}`}`;
        const message = `✅ Gustiel Ideas Backlog exported → ${rootUrl}\n` +
                        `💡 ${ideaResults.length} idea page(s)` +
                        (deletedTitles.length ? ` · 🗑️ ${deletedTitles.length} removed: ${deletedTitles.join(', ')}` : '') + '.';

        return {
            status:   'SUCCESS',
            spaceKey,
            rootUrl,
            rootTitle: ROOT_TITLE,
            ideas:     ideaResults,
            deleted:   deletedTitles,
            message,
        };
    } catch (err) {
        return { status: 'ERROR', message: err.message };
    }
};
