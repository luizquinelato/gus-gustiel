/**
 * Release Notes Resolver — export-release-notes action
 *
 * Builds the three-tier Release Notes hierarchy in Confluence:
 *
 *   📣 Gustiel Release Notes          ← Root page (organizer, no body)
 *     ├── v5                           ← Major bucket (organizer, no body)
 *     │     ├── v5.0 — <title>         ← per-release page (full content)
 *     │     └── v5.1 — <title>
 *     └── v4
 *           ├── v4.55 — <title>
 *           └── v4.54 — <title>
 *
 * Only per-release pages carry content. The root and major-bucket pages are
 * **organizers** — Confluence's native page tree handles the listing. This
 * means deleting a release file from the repo simply leaves its Confluence
 * page orphaned under the right vX bucket; no body needs to be rewritten.
 *
 * All titles are stable (no date / no email) so re-running the export upserts
 * every page in place. Order is newest-first (major desc, minor desc).
 *
 * Source data: RELEASES export from src/docs/index.js, generated by
 * scripts/bundle-docs.mjs from docs/releases/MAJOR.MINOR.md files.
 */

import { getEnvFromJira }                                from '../services/jira-api-service.js';
import { getSpaceByKey, findPageByTitle, createConfluencePage,
         updateConfluencePage, findOrCreatePageByPath }  from '../services/confluence-api-service.js';
import { VERSION }                                       from '../config/constants.js';
import { markdownToStorage, buildTocMacro,
         buildAnchorMacro }                              from '../formatters/confluence-formatter.js';
import { RELEASES }                                      from '../docs/index.js';

// ── Stable titles ────────────────────────────────────────────────────────────

const ROOT_TITLE         = '📣 Gustiel Release Notes';
const majorTitle         = (major) => `v${major}`;
const releaseTitle       = (rel)   => `v${rel.version}${rel.title ? ` — ${rel.title}` : ''}`;

// ── Page body builders ───────────────────────────────────────────────────────

function buildRootMarkdown() {
    return [
        `# 📣 Gustiel — Release Notes`,
        ``,
        `> **Current version:** ${VERSION}`,
        ``,
        `Historical record of changes per release. Browse the child pages below`,
        `(grouped by major version) to read the notes for each release.`,
        ``,
        `For the **current state** of Gustiel, see the **📖 Gustiel User Guide**`,
        `and **🏗️ Gustiel Architecture Guide** pages.`,
    ].join('\n');
}

function buildMajorBucketMarkdown(major) {
    return [
        `# ${majorTitle(major)}`,
        ``,
        `> Release notes for the **v${major}** series. See the child pages below for individual releases.`,
    ].join('\n');
}

function buildReleaseMarkdown(rel) {
    const lines = [
        `# v${rel.version}${rel.title ? ` — ${rel.title}` : ''}`,
        ``,
        `> **Released:** ${rel.date || '_(date not set)_'}`,
        ``,
    ];
    if (rel.whatsNew) {
        lines.push(`## 📣 What's New`, ``, rel.whatsNew, ``);
    }
    if (rel.techChanges) {
        lines.push(`## 🔧 Technical Changes`, ``, rel.techChanges, ``);
    }
    return lines.join('\n');
}

// ── Group RELEASES by major (newest-first preserved from bundler) ────────────

function groupByMajor(releases) {
    const byMajor = new Map();
    for (const r of releases) {
        if (!byMajor.has(r.major)) byMajor.set(r.major, []);
        byMajor.get(r.major).push(r);
    }
    // Convert to sorted array (major desc — bundler already sorts releases newest-first)
    return [...byMajor.entries()]
        .sort((a, b) => b[0] - a[0])
        .map(([major, rels]) => ({ major, releases: rels }));
}

// ── Upsert helper — wraps the 3-tier "find at target / find elsewhere / create" pattern ──

async function upsertPage(spaceId, title, markdown, parentId, parentIsFolder, withToc) {
    const anchor      = buildAnchorMacro('top');
    const toc         = withToc ? buildTocMacro(1, 3) + '\n<hr/>\n' : '';
    const fullContent = markdownToStorage(markdown, { uniformColumns: true });
    const storageBody = `<p>${anchor}</p>\n${toc}${fullContent}`;

    const existingAtTarget = await findPageByTitle(spaceId, title, parentId, parentIsFolder);
    if (existingAtTarget) {
        return await updateConfluencePage(existingAtTarget.id, existingAtTarget.version.number, spaceId, title, storageBody, parentId || null);
    }
    const existingElsewhere = await findPageByTitle(spaceId, title);
    if (existingElsewhere) {
        return await updateConfluencePage(existingElsewhere.id, existingElsewhere.version.number, spaceId, title, storageBody, parentId || null);
    }
    // v2 POST applies stricter editor validation than PUT — create a stub then update.
    const stub = await createConfluencePage(spaceId, title, '<p>Generating…</p>', parentId);
    return await updateConfluencePage(stub.id, 1, spaceId, title, storageBody, parentId || null);
}

// ── Resolver ─────────────────────────────────────────────────────────────────

export const exportReleaseNotes = async (event) => {
    const spaceKey   = (event?.payload?.spaceKey   || event?.spaceKey   || '').trim().toUpperCase();
    const parentPath = (event?.payload?.parentPath || event?.parentPath || '').trim();
    const folderId   = (event?.payload?.folderId   || event?.folderId   || '').trim();

    if (!spaceKey) return { status: 'ERROR', message: 'Please provide a Confluence space key (e.g. GUSTIEL).' };

    let env;
    try {
        env = await getEnvFromJira();
    } catch (e) {
        return { status: 'ERROR', message: `Could not detect Jira environment: ${e.message}` };
    }

    if (!Array.isArray(RELEASES) || RELEASES.length === 0) {
        return { status: 'ERROR', message: 'No release notes available. Add files under `docs/releases/` and run `.\\deploy.ps1 --docs`.' };
    }

    try {
        const space   = await getSpaceByKey(spaceKey);
        const majors  = groupByMajor(RELEASES);

        // ── Resolve top-level parent (where the Release Notes root page lives) ──
        let topParentId = null, topParentIsFolder = false;
        if (folderId) {
            topParentId       = folderId;
            topParentIsFolder = true;
        } else if (parentPath) {
            topParentId       = await findOrCreatePageByPath(space.id, parentPath);
            topParentIsFolder = false;
        }

        // ── Tier 1: Root page (organizer, no body content) ────────────────────
        const rootPage = await upsertPage(space.id, ROOT_TITLE, buildRootMarkdown(), topParentId, topParentIsFolder, false);

        // ── Tier 2 + 3: Major buckets (organizers) and per-release pages ──────
        const majorResults   = [];
        const releaseResults = [];
        for (const m of majors) {
            const majorPage = await upsertPage(space.id, majorTitle(m.major), buildMajorBucketMarkdown(m.major), rootPage.id, false, false);
            majorResults.push({ major: m.major, pageId: majorPage.id });

            for (const rel of m.releases) {
                const relPage = await upsertPage(space.id, releaseTitle(rel), buildReleaseMarkdown(rel), majorPage.id, false, true);
                releaseResults.push({ version: rel.version, title: rel.title, pageId: relPage.id });
            }
        }

        const rootUrl = `${env.baseUrl}/wiki${rootPage._links?.webui || `/spaces/${spaceKey}/pages/${rootPage.id}`}`;
        const message = `✅ Gustiel Release Notes exported → ${rootUrl}\n` +
                        `📚 ${majorResults.length} major bucket(s) · ${releaseResults.length} release page(s).`;

        return {
            status:    'SUCCESS',
            spaceKey,
            rootUrl,
            rootTitle: ROOT_TITLE,
            majors:    majorResults,
            releases:  releaseResults,
            message,
        };
    } catch (err) {
        return { status: 'ERROR', message: err.message };
    }
};
